export function findQuerycontinentCoordinates(continentName) {
  const keyFound = Object.keys(regionsCoordinates).find((k) => {
    return continentName.match(new RegExp(k, 'i'))
  })
  if (keyFound) {
    return regionsCoordinates[keyFound]
  }
}
export function getCountryCodeByCountryName(countryName) {
  const country = countries.find(
    (country) => country.dxName === countryName || country.queryName === countryName
  )
  return country?.queryCode || ''
}

export function getQueryCountryKey(countryKey) {
  const country = countries.find((country) => country.queryCode === countryKey) || {}
  return country.coordinates
}

export function findDXCountryName(bexCountryName) {
  let country = countries.find((country) => country.queryName === bexCountryName)
  if (country === undefined)
    country = countries.find((country) => country.queryCode === bexCountryName)

  return country?.dxName || ''
}

export function findCoordinates(bexCountryName) {
  let country = countries.find((country) => country.queryName === bexCountryName)
  if (country === undefined)
    country = countries.find((country) => country.queryCode === bexCountryName)
  if (typeof country === 'object' && Array.isArray(country.coordinates)) {
    const [
      x,
      y,
    ] = country.coordinates

    return [
      y,
      x,
    ]
  }
  return null
}

